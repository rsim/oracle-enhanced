        private
          def initialize_type_map(m)
            super
            # oracle
            register_class_with_precision m, %r(WITH TIME ZONE)i,       Type::OracleEnhanced::TimestampTz
            register_class_with_precision m, %r(WITH LOCAL TIME ZONE)i, Type::OracleEnhanced::TimestampLtz
            register_class_with_limit m, %r(raw)i,            Type::OracleEnhanced::Raw
            register_class_with_limit m, %r{^(char)}i,        Type::OracleEnhanced::CharacterString
            register_class_with_limit m, %r{^(nchar)}i,       Type::OracleEnhanced::String
            register_class_with_limit m, %r(varchar)i,        Type::OracleEnhanced::String
            register_class_with_limit m, %r(clob)i,           Type::OracleEnhanced::Text
            register_class_with_limit m, %r(nclob)i,           Type::OracleEnhanced::NationalCharacterText

            m.register_type "NCHAR", Type::OracleEnhanced::NationalCharacterString.new
            m.alias_type %r(NVARCHAR2)i,    "NCHAR"

            m.register_type(%r(NUMBER)i) do |sql_type|
              scale = extract_scale(sql_type)
              precision = extract_precision(sql_type)
              limit = extract_limit(sql_type)
              if scale == 0
                Type::OracleEnhanced::Integer.new(precision: precision, limit: limit)
              else
                Type::Decimal.new(precision: precision, scale: scale)
              end
            end

            if OracleEnhancedAdapter.emulate_booleans
              m.register_type %r(^NUMBER\(1\))i, Type::Boolean.new
            end
          end
      end

      def extract_value_from_default(default)
        case default
        when String
          default.gsub("''", "'")
        else
          default
        end
      end

      def extract_limit(sql_type) # :nodoc:
        case sql_type
        when /^bigint/i
          19
        when /\((.*)\)/
          $1.to_i
        end
      end

      # create bind object for type String
      def bind_string(name, value)
        ActiveRecord::Relation::QueryAttribute.new(name, value, Type::OracleEnhanced::String.new)
      end

      ActiveRecord::Type.register(:boolean, Type::OracleEnhanced::Boolean, adapter: :oracle_enhanced)
      ActiveRecord::Type.register(:json, Type::OracleEnhanced::Json, adapter: :oracle_enhanced)

      private
        def prefetch_primary_key_from_schema_cache(table_name)
          pk_col = primary_key_column_from_schema_cache(table_name)
          # Propagate false (cache says no-prefetch) and nil (caller must fall back) as-is.
          return pk_col unless pk_col.is_a?(OracleEnhanced::Column)
          !(pk_col.auto_incremented_by_db? || pk_col.auto_populated_on_insert?)
        end

        def primary_key_column_from_schema_cache(table_name)
          return nil unless schema_cache.data_source_exists?(table_name)

          pks = schema_cache.primary_keys(table_name)
          return false if composite_primary_key?(pks)
          return false if pks.nil?

          pk_col = schema_cache.columns_hash(table_name)[pks]
          pk_col if column_with_pk_metadata?(pk_col)
        end

        def composite_primary_key?(pks)
          pks.is_a?(Array)
        end

        def column_with_pk_metadata?(pk_col)
          pk_col.is_a?(OracleEnhanced::Column) &&
            pk_col.instance_variable_defined?(:@identity) &&
            pk_col.instance_variable_defined?(:@trigger_assigned)
        end

        def prefetch_primary_key_from_dictionary(table_name)
          owner, desc_table_name = resolve_data_source_name(table_name)
          pks                   = primary_keys(table_name)
          # Composite PKs cannot be prefetched from a single sequence; mirror the
          # schema-cache path's behaviour and fall through to the RETURNING-driven
          # insert path instead. Note: cannot use `composite_primary_key?(pks)`
          # here — that predicate is `Array?`-based and `primary_keys` always
          # returns an Array (including `["id"]` for single-column PKs).
          return false if pks.size > 1
          has_identity_pk       = supports_identity_columns? && identity_primary_key?(owner, desc_table_name)
          has_trigger_backed_pk = trigger_backed_primary_key?(owner, desc_table_name)
          pks.any? && !has_identity_pk && !has_trigger_backed_pk
        end

        AREL_VISITOR_MODES = %i[auto rownum fetch_first].freeze
        private_constant :AREL_VISITOR_MODES

        IDENTIFIER_MAX_LENGTH_MODES = %i[auto short long].freeze
        private_constant :IDENTIFIER_MAX_LENGTH_MODES

        # Per-connection +identifier_max_length+ is the canonical source. The
        # deprecated global +use_shorter_identifier+ (read via @@class var to
        # skip its deprecation warning on every lookup) is honored only when
        # the connection config does not set +identifier_max_length+ itself;
        # an explicit per-connection value always wins.
        #
        # Type and value are validated up front — unknown values and
        # non-Symbol / non-String inputs raise +ArgumentError+ here.
        def resolve_identifier_max_length
          raw = @config[:identifier_max_length]
          if raw.nil?
            return @@use_shorter_identifier ? :short : :auto
          end
          unless raw.is_a?(String) || raw.is_a?(Symbol)
            raise ArgumentError,
              "identifier_max_length must be a String or Symbol (got #{raw.inspect}). " \
              "Expected #{identifier_max_length_modes_list}."
          end
          mode = raw.to_sym
          unless IDENTIFIER_MAX_LENGTH_MODES.include?(mode)
            raise ArgumentError,
              "Unknown identifier_max_length #{mode.inspect}. " \
              "Expected #{identifier_max_length_modes_list}."
          end
          mode
        end

        def identifier_max_length_modes_list
          symbols = IDENTIFIER_MAX_LENGTH_MODES.map { |mode| ":#{mode}" }
          "#{symbols[0..-2].join(', ')}, or #{symbols.last}"
        end

        def arel_visitor
          case resolved_arel_visitor_mode
          when :fetch_first
            Arel::Visitors::Oracle12.new(self)
          when :rownum
            Arel::Visitors::Oracle.new(self)
          end
        end

        def resolved_arel_visitor_mode
          return :rownum unless _connection

          mode = configured_arel_visitor_mode

          if mode == :fetch_first && !supports_fetch_first_n_rows_and_offset?
            raise ArgumentError,
              "arel_visitor: :fetch_first requires Oracle 12.1 or later " \
              "(connected server reports #{database_version}). " \
              "Omit the key for version-aware selection, or use :rownum to force ROWNUM-based LIMIT/OFFSET."
          end

          return mode unless mode == :auto
          supports_fetch_first_n_rows_and_offset? ? :fetch_first : :rownum
        end

        def configured_arel_visitor_mode
          per_connection = @config[:arel_visitor] if @config.key?(:arel_visitor)

          if per_connection.nil?
            self.class.use_old_oracle_visitor ? :rownum : :auto
          else
            unless per_connection.is_a?(String) || per_connection.is_a?(Symbol)
              raise ArgumentError, "arel_visitor must be a String or Symbol (got #{per_connection.inspect}). Expected one of #{AREL_VISITOR_MODES.inspect}."
            end
            mode = per_connection.to_sym
            unless AREL_VISITOR_MODES.include?(mode)
              raise ArgumentError, "Unknown arel_visitor #{mode.inspect}. Expected one of #{AREL_VISITOR_MODES.inspect}."
            end
            mode
          end
        end

        def build_statement_pool
          StatementPool.new(self.class.type_cast_config_to_integer(@config[:statement_limit]))
        end

        def type_map
          self.class.type_map
        end

        def translate_exception(exception, message:, sql:, binds:)
          return ActiveRecord::ConnectionFailed.new(message, sql: sql, binds: binds, connection_pool: @pool) if _connection.lost_connection?(exception)

          case _connection.error_code(exception)
          when 1
            RecordNotUnique.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 60
            Deadlocked.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 900, 904, 942, 955, 1418, 2289, 2449, 17008
            ActiveRecord::StatementInvalid.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 1400
            ActiveRecord::NotNullViolation.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 2290
            ActiveRecord::CheckViolation.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 2291, 2292
            InvalidForeignKey.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 8177
            ActiveRecord::SerializationFailure.new(message, sql: sql, binds: binds, connection_pool: @pool)
          when 12899
            ValueTooLong.new(message, sql: sql, binds: binds, connection_pool: @pool)
          else
            super
          end
        end
    end
  end
end

